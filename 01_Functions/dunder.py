#Dunder functions are special methods that allow Python objects to interact with built-in functions and operators.

# e.g __init__ is a dunder method used to initialize a new object.
# e.g __str__ is a dunder method used to define the string representation of an object.
# e.g __add__ is a dunder method used to define the behavior of the addition operator (+) for an object.
# e.g __len__ is a dunder method used to define the behavior of the len() function for an object.
# e.g __eq__ is a dunder method used to define the behavior of the equality operator (==) for an object.
# e.g __getitem__ is a dunder method used to define the behavior of indexing (e.g., obj[key]) for an object.